module determinant_by_Gramm_Schmidt
implicit none

    contains
    
     
    !==============================================================================
! DSTAR - Вычисление определителя комплексной матрицы
!         методом двойной ортогонализации Грама-Шмидта
!
! Параметры:
!   A(ND,N) - входная комплексная матрица
!   C(ND,N) - выходная ортонормированная матрица (рабочая)
!   S(N)    - рабочий вектор
!   SM(N)   - рабочий вектор
!   DET     - выходной определитель (комплексный)
!   ND      - объявленная размерность матриц (leading dimension)
!   N       - фактический размер матрицы (N x N)
!   KORT    - количество циклов ортогонализации (обычно 2-3)
!==============================================================================

    subroutine get_determinant_by_Gramm_Schmidt(A, C, S, SM, DET, ND, N, KORT)
    implicit none
    
    ! Входные/выходные параметры
    integer(8), intent(in) :: ND, N, KORT
    complex(8), intent(in) :: A(ND, N)
    complex(8), intent(out) :: C(ND, N)
    complex(8), intent(out) :: S(N), SM(N)
    complex(8), intent(out) :: DET
    
    ! Локальные переменные
    integer(8) :: k, k1, kw, jr, i
    real(8) :: norm, residual_norm, product_norm
    complex(8) :: proj
    
    ! Константы
    real(8), parameter :: EPS = 1.0D-15
    
    ! Инициализация накопленного произведения норм
    product_norm = 0.0d0
    
    !==========================================================================
    ! 1. ОРТОГОНАЛИЗАЦИЯ ГРАМА-ШМИДТА
    !    Построение ортонормированного базиса и накопление произведения норм
    !==========================================================================
    
    do k = 1, N
        k1 = k - 1
        kw = 0
        
        ! Первый столбец - отдельная обработка
        if (k == 1) then
            ! Копируем первый столбец в S и SM
            do i = 1, N
                S(i) = A(i, 1)
                SM(i) = A(i, 1)
            enddo
            
            ! Вычисляем норму первого столбца
            norm = sqrt(sum(S(1:N) * conjg(S(1:N))))
            
            ! Проверка на вырожденность
            if (norm <= EPS) then
                DET = (0.0d0, 0.0d0)
                return
            endif
            
            ! Нормируем первый столбец
            C(1:N, 1) = S(1:N) / norm
            
            ! Инициализируем произведение норм
            product_norm = norm
            cycle
        endif
        
        !======================================================================
        ! Для остальных столбцов: ортогонализация относительно предыдущих
        !======================================================================
        
        ! Копируем k-й столбец в S и SM
        do i = 1, N
            S(i) = A(i, k)
            SM(i) = A(i, k)
        enddo
        
        ! Повторяем ортогонализацию KORT раз
        do
            ! Вычитаем проекции на все предыдущие ортонормированные векторы
            do jr = 1, k1
                ! Вычисляем проекцию
                proj = (0.0d0, 0.0d0)
                do i = 1, N
                    proj = proj + S(i) * conjg(C(i, jr))
                enddo
                
                ! Вычитаем проекцию
                do i = 1, N
                    SM(i) = SM(i) - proj * C(i, jr)
                enddo
            enddo
            
            ! Вычисляем норму после ортогонализации
            residual_norm = sqrt(sum(abs(SM(1:N))**2))
            
            ! Обновляем произведение норм
            product_norm = product_norm * residual_norm
            
            ! Проверка на вырожденность
            if (residual_norm <= EPS) then
                DET = (0.0d0, 0.0d0)
                return
            endif
            
            ! Нормируем ортогональный вектор
            do i = 1, N
                S(i) = SM(i) / residual_norm
                SM(i) = SM(i) / residual_norm
            enddo
            
            kw = kw + 1
            
            ! Повторяем ортогонализацию при необходимости
            if (kw < KORT) then
                cycle
            endif
            exit
        enddo
        
        ! Сохраняем ортонормированный вектор
        C(1:N, k) = S(1:N)
        
    enddo
    
    !==========================================================================
    ! 2. ВЫЧИСЛЕНИЕ ОПРЕДЕЛИТЕЛЯ ОРТОНОРМИРОВАННОЙ МАТРИЦЫ
    !    Используем DGAUS для матрицы C (det(C) = ±1)
    !==========================================================================
    
    call DGAUS(C, ND, N, DET)
    
    !==========================================================================
    ! 3. ВОССТАНОВЛЕНИЕ ИСХОДНОГО ОПРЕДЕЛИТЕЛЯ
    !    det(A) = det(C) * ∏(нормы столбцов)
    !==========================================================================
    
    DET = DET * product_norm
    
    return
    end subroutine get_determinant_by_Gramm_Schmidt
    
    

end module determinant_by_Gramm_Schmidt
